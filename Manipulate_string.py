#replace_comas_with_space for text(input)
def replace_comas_with_space(text):
    y = text.replace(","," ")
    return y

#split_sentence for sentence(input)
def split_sentence(sentence):
    z = sentence.split()
    return z

#join_words for words(input)
def join_words(words):
    x = " ".join(words)
    return x


